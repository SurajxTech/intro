
# SURAJ Pattern Script

def print_suraj():
    pattern = [
        " *****  *****  *****   ***   ***** ",
        "*      *      *      *   *  *     ",
        "*****  *****  *****  *****  ***** ",
        "    *      *      *  *   *      * ",
        "*****  *****  *****  *   *  ***** "
    ]
    
    for line in pattern:
        print(line)

def main():
    print("Displaying the name 'SURAJ' using '*' characters:\n")
    print_suraj()

if __name__ == "__main__":
    main()
